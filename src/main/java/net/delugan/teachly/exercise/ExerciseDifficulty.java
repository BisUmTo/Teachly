package net.delugan.teachly.exercise;

/**
 * Enumeration of possible difficulty levels for exercises.
 * Used to categorize exercises based on their complexity.
 */
public enum ExerciseDifficulty {
    /**
     * Represents an easy difficulty level.
     * Suitable for beginners or introductory concepts.
     */
    EASY,
    
    /**
     * Represents a medium difficulty level.
     * Suitable for intermediate learners or concepts requiring some prior knowledge.
     */
    MEDIUM,
    
    /**
     * Represents a hard difficulty level.
     * Suitable for advanced learners or complex concepts.
     */
    HARD
}
